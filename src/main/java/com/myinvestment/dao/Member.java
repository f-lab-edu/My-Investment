package com.myinvestment.dao;

public class Member {


	private Long id;
	private String email;
	private String nickname;
	private String password;



	public Long getId(){
		return id;
	}

	public String getEmail() {
		return email;
	}

	public String getNickname() {
		return nickname;
	}

	public String getPassword() {
		return password;
	}



}
